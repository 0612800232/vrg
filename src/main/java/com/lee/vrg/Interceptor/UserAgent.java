package com.lee.vrg.Interceptor;

import java.util.Date;

public class UserAgent {
	
	private Integer id;
	
	private String userName;

	private String mobileNo;

	private String email;

	private String logo;

	private String sex;

	private Date birthday;

	private String realName;

	private String cardNo;

	private String hometownName;

	private String hometownAddressX;

	private String hometownAddressY;
	
	

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getMobileNo() {
		return mobileNo;
	}

	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getLogo() {
		return logo;
	}

	public void setLogo(String logo) {
		this.logo = logo;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public Date getBirthday() {
		return birthday;
	}

	public void setBirthday(Date birthday) {
		this.birthday = birthday;
	}

	public String getRealName() {
		return realName;
	}

	public void setRealName(String realName) {
		this.realName = realName;
	}

	public String getCardNo() {
		return cardNo;
	}

	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}

	public String getHometownName() {
		return hometownName;
	}

	public void setHometownName(String hometownName) {
		this.hometownName = hometownName;
	}

	public String getHometownAddressX() {
		return hometownAddressX;
	}

	public void setHometownAddressX(String hometownAddressX) {
		this.hometownAddressX = hometownAddressX;
	}

	public String getHometownAddressY() {
		return hometownAddressY;
	}

	public void setHometownAddressY(String hometownAddressY) {
		this.hometownAddressY = hometownAddressY;
	}

}
